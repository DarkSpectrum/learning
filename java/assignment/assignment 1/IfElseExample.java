class IfElseExample {
    public static void main(String args[]) {
        int num = 80;
        if (num > 50) {
            System.out.println("Greater than 50!");
        } else {
            System.out.println("Less than 50!");
        }
    }
}  
   

/*
Explanation

1) Declare a number
2) If the number is greater than 50 (in this case it is) run this code
3) If it isn't, run this code (this code doesn't run)
*/